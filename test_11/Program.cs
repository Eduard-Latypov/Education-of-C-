Console.Clear();
for (int i = 0; i <= 10; i++)
{
    System.Console.Write(i + " ");
}
System.Console.WriteLine();
for (int i = 0, j = 0; i < 10; i++, j++)
{
    System.Console.Write(i + " ");
    System.Console.Write(j + " ");
}
System.Console.WriteLine();
for (int i = 0, j = 5; i < 10 && j < 12; i++, j++)
{
    System.Console.Write(i + " ");
    System.Console.Write(j + " ");
}
System.Console.WriteLine();
for (int i = 0, j = 5; i < 10 || j < 12; i++, j++)
{
    System.Console.Write(i + " ");
    System.Console.Write(j + " ");
}
System.Console.WriteLine();
for (int i = 10, j = 0; i >= 0; i--, j++)
{
    System.Console.Write(i + " ");
    System.Console.Write(j + " ");
}
