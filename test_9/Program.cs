// цикл do while

int count = 5;
do
{
    count++;
    System.Console.WriteLine(count);
} while (count < 5);
// цикл do while отличается от классического while тем, что while сначало проверяет условие, и если условие истинно, то уже после выполняет действие, а do while сначало выполняет действие а после уже проверяет условие, даже если условие ложно, то do while в любом случае успеет выполнить хоть одно действие! 